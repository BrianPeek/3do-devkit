#ifndef TYPES_INTS_H_INCLUDED
#define TYPES_INTS_H_INCLUDED

/* 8bit */
typedef signed char   i8;
typedef unsigned char u8;
typedef i8            int8;
typedef i8            sint8;
typedef i8            schar;
typedef i8            sbyte;
typedef u8            uint8;
typedef u8            uchar;
typedef u8            ubyte;

/* 16bit */
/* generally avoid 16bit ints */
typedef signed short   i16;
typedef unsigned short u16;
typedef i16            int16;
typedef i16            sint16;
typedef u16            uint16;
typedef u16            unichar;

/* 32bit */
typedef signed long   i32;
typedef unsigned long u32;
typedef i32           int32;
typedef i32           sint32;
typedef u32           uint32;
typedef u32           ulong;

#endif
